package org.baibei.keyboardtrackertgbot.pojo.other;

public class BotToken {

    private String token;

    public BotToken(String token) {
        this.token = token;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public String toString() {
        return token;
    }
}
